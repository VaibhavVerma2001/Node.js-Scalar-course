// information about current system
// can be used to check system compatibility
// for example a game website is checking whether our system is compatible or not

const os = require("os");

console.log(os.arch()); // architecture // x64
console.log(os.platform()); // platform // win32

// console.log(os.networkInterfaces()); // network
// console.log(os.cpus()); // all details of cpus, such as graphics, processor etc

console.log(os.totalmem()); // total memory
console.log(os.freemem()); // free memory


console.log(os.hostname()); // LAPTOP-FQ7VDHGN
console.log(os.homedir()); // C:\Users\vaibh
console.log(os.type()); // Windows_NT
// console.log(os.userInfo()); // Windows_NT

console.log(os.uptime()); // system uptime in seconds

console.log(os.userInfo());
